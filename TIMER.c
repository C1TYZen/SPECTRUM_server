/* ******************************************************
 * Настройка таймера требуется для работы 
 * функции millis();
 * 1. Разрешаем прерывание от данного таймера 
 * 2. Задаем делитель частоты 1024 и запускаем таймер
 ********************************************************/
void TIMER_init() {
	TIMSK0 |= (1<<TOIE0);
	TCCR0B |= (1<<CS02) | (1<<CS00);
}